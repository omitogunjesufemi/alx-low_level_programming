/**
 *_putchar - This is for printing characters
 * @c: character
 * Return: int
 */
int _putchar(char c);

/**
 * _isupper - This checks for uppercase characters
 * @c: int
 * Return: int
 */
int _isupper(int c);

/**
 * _isdigit - This checks for a digit (0 through 9)
 * @c: int
 * Return: int
 */
int _isdigit(int c);

/**
 * mul - This multiplies two integers
 * @a: int
 * @b: int
 * Return: int
 */
int mul(int a, int b);

/**
 * print_numbers - This prints the numbers from 0 to 9, followed by a new line
 */
void print_numbers(void);

/**
 * print_most_numbers - This prints the numbers from 0 to 9, excluding 2 and 4.
 */
void print_most_numbers(void);

/**
 * more_numbers - This prints the numbers from 0 to 9, 10 times.
 */
void more_numbers(void);

/**
 * print_line - This draws a straight line in the terminal
 * @n: int
 */
void print_line(int n);

/**
 * print_diagonal - This draws a slant line in the terminal
 * @n: int
 */
void print_diagonal(int n);

/**
 * print_squares - This prints a square
 * @n: int
 */
void print_square(int size);

/**
 * print_triangle - This prints a triangle
 * @n: int
 */
void print_triangle(int size);

/**
 * print_number - This prints the value of an integer
 * @n: value to be printed
 */
void print_number(int n);
